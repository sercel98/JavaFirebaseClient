    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        
          if(txtIdentificador.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Por favor escriba un c√≥digo", "Aviso", JOptionPane.ERROR_MESSAGE);
        }
        else
        {
   
                   
        String identificador;
        String nombre;
        double precio;
        String marca; 
        
        identificador = txtIdentificador.getText(); 
        int idProveedor = Integer.parseInt(txtIdProveedor.getText());
        
        Dulce d = servicio.buscarDulce(idProveedor, identificador);

        if (d == null) {
            JOptionPane.showMessageDialog(this, "No se encontraron dulces con ese c√≥digo", "Aviso", JOptionPane.ERROR_MESSAGE);
            txtIdentificador.setText("");
            txtNombre.setText("");
            txtPrecio.setText("");
            txtMarca.setText("");
        
            return;
        }
                  
        txtIdProveedor.setText(idProveedor + "");
        txtIdentificador.setText(d.getIdentificacion() + "");
        txtNombre.setText(d.getNombre() + "");
        txtPrecio.setText(d.getPrecio() + "");
        txtMarca.setText(d.getMarca() + "");
        
        txtIdentificador.setEditable(false);
        txtNombre.setEditable(true);
        txtPrecio.setEditable(true);
        txtMarca.setEditable(true);
        btnModificar.setEnabled(true);
    }
        
        
        
    }//GEN-LAST:event_btnBuscarActionPerformed

