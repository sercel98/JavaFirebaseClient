    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        if(txtIdentificador.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Por favor escriba un c贸digo", "Aviso", JOptionPane.ERROR_MESSAGE);
        }
        else
        {
        int idProveedor;
        Dulce d;
        String identificacion; 

        identificacion = (txtIdentificador.getText());

        idProveedor = Integer.parseInt(txtIdProveedor.getText());

        d = servicio.buscarDulce(idProveedor,identificacion );

        if (d == null) {
            JOptionPane.showMessageDialog(this, "No se encontraron dulces con ese c贸digo", "Aviso", JOptionPane.ERROR_MESSAGE);
            txtIdentificador.setText("");
            txtNombre.setText("");
            txtPrecio.setText("");
            txtMarca.setText("");

            return;
        }



        txtIdentificador.setText(d.getIdentificacion()+ "");
        txtNombre.setText(d.getNombre() + "");
        txtPrecio.setText(d.getPrecio() + "");
        txtMarca.setText(d.getMarca()+ "");
        txtIdentificador.setEditable(false);     
        }
        
        
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        
        if(txtIdentificador.getText().equals(""))
       {
            JOptionPane.showMessageDialog(this, "Por favor escriba un c贸digo", "Aviso", JOptionPane.ERROR_MESSAGE);
       }
       else
       {
       
        String identificador;
        identificador =txtIdentificador.getText();
        
        int idProveedor = Integer.parseInt(txtIdProveedor.getText()); 

        Dulce d = servicio.buscarDulce(idProveedor, identificador);

        if (d == null) {
            JOptionPane.showMessageDialog(this, "No se encontraron dulces con ese c贸digo", "Aviso", JOptionPane.ERROR_MESSAGE);
            txtIdentificador.setText("");
            txtNombre.setText("");
            txtPrecio.setText("");
            txtMarca.setText("");

            return;
        } else {
            
            servicio.eliminarDulce(idProveedor, d.getIdentificacion());

            txtIdProveedor.setText("");
            txtIdentificador.setText("");
            txtNombre.setText("");
            txtPrecio.setText("");
            txtMarca.setText("");

            txtIdentificador.setEditable(true);
            
            JOptionPane.showMessageDialog(this, "Dulce eliminado exitosamente", "Aviso", JOptionPane.INFORMATION_MESSAGE);
        }   
    }
        
        
    }//GEN-LAST:event_btnEliminarActionPerformed

